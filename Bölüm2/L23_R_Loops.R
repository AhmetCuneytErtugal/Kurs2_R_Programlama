## FOR

# for(iterasyon sayisi){
#   aksiyon
# }

for(i in 1:5){
  print("R Programlama")
}

for (j in 5:10) {
  print(j)
}



## WHILE

# while(logical expression){
#   action statement
#   counter 
# }


while(FALSE){
  print("Data Forest R Programlama")
}

while(TRUE){
  print("Data Forest R Programlama")
}



i = 1
while(i < 5){
  print("R Programlamaya Hosgeldiniz")
  i = i + 1
}


j = 1
while(j < 4){
  print(j)
  j = j + 1
}




## NESTED LOOPS(Ic ice Donguler)
i = 1
j = 1
for (i in 1:3) {
